    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         Conexxion  cc = new Conexxion("root","","arantza","localhost");
         String path ="C:\\Arantza\\reporte.jasper";
         JasperReport jr;
        try {
            jr=(JasperReport) JRLoader.loadObjectFromFile(path);
            JasperPrint jp = JasperFillManager.fillReport(jr, null, cc.getConexion());
            JasperViewer jv = new JasperViewer(jp);
            jv.setVisible(true);
            jv.setTitle(path);
           
        } catch (JRException ex) {
            Logger.getLogger(VMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
              
    }//GEN-LAST:event_jButton1ActionPerformed

